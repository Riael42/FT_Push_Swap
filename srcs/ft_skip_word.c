#include "push_swap.h"

int	ft_skip_word(char *s, int curr)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < curr && s[j] != '\0')
	{
		while (s[j] != ' ')
			j += 1;
		while (s[j] == ' ')
			j += 1;
		i += 1;
	}
	return (j);
}
