#include "push_swap.h"

int	ft_isspace(char c, int type)
{
	if (type == 1)
		return (c == ' ');
	if (type == 2)
		return (c == ' ' || c == '\t' || c == '\n');
	if (type == 3)
		return (c == ' ' || c == '\f' || c == '\r'
				|| c == '\t' || c == '\v');
	return (0);
}
